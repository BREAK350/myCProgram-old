float T_Point3::Scalar(const T_Point3 &p)
{
    return (x*p.x+y*p.y+z*p.z);
}
//---------------------------------------------------------------------------
T_Point3 T_Point3::Vector(const T_Point3 &p)
{
    return T_Point3(y*p.z-z*p.y,z*p.x-x*p.z,x*p.y-y*p.x);
}
