struct T_Data
{
    TColor Color;
    float Z;
    T_Data() { Color=clBlack; Z=100000; }
};
//---------------------------------------------------------------------------
class T_Display
{
    private:
        int N,M;
        T_Data **Data;
        float Zmin,Zmax;
    public:
        T_Display();
        T_Display(const int &n,const int &m);
        T_Display(const T_Display &Display);
        ~T_Display();
        T_Display& operator = (const T_Display &Display);
        void Draw(TCanvas *Canvas,const int &x,const int &y);
        void SetData(const int &i,const int &j,const T_Data &D);
        int GetN()const { return N; }
        int GetM()const { return M; }
        void SetZmin(const float &zmin) { Zmin=zmin; }
        void SetZmax(const float &zmax) { Zmax=zmax; }
};
