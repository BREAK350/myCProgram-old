//---------------------------------------------------------------------------
struct T_Triangle
{
    int Point[3];
    void SetPoint(const int &p0,const int &p1,const int &p2)
    {
        Point[0]=p0;
        Point[1]=p1;
        Point[2]=p2;
    }
    void SaveToFile(ofstream &ofs)
    {
        ofs<<Point[0]<<' '<<Point[1]<<' '<<Point[2];
    }
    void LoadFromFile(ifstream &ifs)
    {
        ifs>>Point[0]>>Point[1]>>Point[2];
    }
};
//---------------------------------------------------------------------------
class T_Object
{
    private:
        T_Point3s Const,Buf;
        T_Array <T_Triangle> Borders;
        void DrawTriangle(const int &i,T_Display &Display,const int &x,const int &y);
    public:
        T_Object():Const(),Buf(),Borders() {}
        T_Object(const T_Point3s &_Const,const T_Array <T_Triangle> &_Borders):
            Const(_Const),Buf(_Const),Borders(_Borders) {}
        void DoMatrix(const T_Matrix4 &M);
        void Draw(T_Display &Display,const int &x,const int &y);
        void SaveToFile(ofstream &ofs);
        void LoadFromFile(ifstream &ifs);
};
