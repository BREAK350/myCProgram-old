T_TreeItem2::T_TreeItem2()
{
    AnyValue=NULL;
    First=Second=Third=Fourth=NULL;
}
//---------------------------------------------------------------------------
void T_TreeItem2::AddValue(T_TreeAnyValue *V)
{
    if(AnyValue)
    {
        if(V->GetFirstParameter()>AnyValue->GetFirstParameter())
        {
            if(V->GetSecondParameter()>AnyValue->GetSecondParameter())
            {
                if(!First)
                {
                    First=new T_TreeItem2;
                }
                First->AddValue(V);
            }
            else
            {
                if(!Fourth)
                {
                    Fourth=new T_TreeItem2;
                }
                Fourth->AddValue(V);
            }
        }
        else
        {
            if(V->GetSecondParameter()>AnyValue->GetSecondParameter())
            {
                if(!Second)
                {
                    Second=new T_TreeItem2;
                }
                Second->AddValue(V);
            }
            else
            {
                if(!Third)
                {
                    Third=new T_TreeItem2;
                }
                Third->AddValue(V);
            }
        }
    }
    else
    {
        AnyValue=V;
    }
}
