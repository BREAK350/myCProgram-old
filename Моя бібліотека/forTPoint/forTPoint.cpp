bool operator == (const TPoint &p1,const TPoint &p2)
{
	return p1.x==p2.x && p1.y==p2.y;
}
//---------------------------------------------------------------------------
bool operator != (const TPoint &p1,const TPoint &p2)
{
	return !(p1==p2);
}
//---------------------------------------------------------------------------
TPoint operator + (const TPoint &p1,const TPoint &p2)
{
	return TPoint(p1.x+p2.x,p1.x+p2.x);
}
//---------------------------------------------------------------------------
TPoint operator - (const TPoint &p1,const TPoint &p2)
{
	return TPoint(p1.x-p2.x,p1.x-p2.x);
}
//---------------------------------------------------------------------------
double SquareLength(const TPoint &p)
{
	return p.x*p.x+p.y*p.y;
}
//---------------------------------------------------------------------------
double Length(const TPoint &p)
{
	return sqrt(SquareLength(p));
}
