//---------------------------------------------------------------------------
TbDetail::TbDetail(TbDetail *Parent)
{
	this->Parent=Parent;
}
//---------------------------------------------------------------------------
TbVector2d TbDetail::GetGlobalLocation()const
{
	return GetLocation()+GetParentLocation();
}
//---------------------------------------------------------------------------
TbVector2d TbDetail::GetLocation()const
{
	return Location;
}
//---------------------------------------------------------------------------
TbVector2d TbDetail::GetParentLocation()const
{
	return (Parent)?(Parent->GetGlobalLocation()):(TbVector2d(0,0));
}
//---------------------------------------------------------------------------
void TbDetail::SetLocation(const TbVector2d &Location)
{
	this->Location=Location;
}
