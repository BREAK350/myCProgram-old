//---------------------------------------------------------------------------
TbGraphicControl::TbGraphicControl(TTimer *Timer)
{
    this->Timer=Timer;
}
//---------------------------------------------------------------------------
void TbGraphicControl::Draw()
{
    List.CurrSetFirst();
    while(List.CurrCorrect())
    {
        List.GetCurr()->Draw(Or);
        List.CurrSetNext();
    }
}
//---------------------------------------------------------------------------
void TbGraphicControl::NextPos()
{
    List.CurrSetFirst();
    while(List.CurrCorrect())
    {
        if(List.GetCurr()->Enabled)
        {
            List.GetCurr()->NextPos();
            List.CurrSetNext();
        }
        else
        {
            delete List.GetCurr();
            List.DeleteCurr();
        }
    }
}
//---------------------------------------------------------------------------
void TbGraphicControl::Execute()
{
    Draw();
    NextPos();
}
