void TbElement::Print1()
{
    int i=Index;
    cout<<Symbol<<abs(i);
}
//---------------------------------------------------------------------------
void TbElement::Print2()
{
    int i=Index;
    cout<<Symbol<<'_'<<abs(i);
}
//---------------------------------------------------------------------------
bool TbElement::operator == (const TbElement &El)
{
    return Symbol==El.Symbol && Index==El.Index;
}
//---------------------------------------------------------------------------
bool TbElement::operator != (const TbElement &El)
{
    return !(*this==El);
}
//---------------------------------------------------------------------------
TbElement::TbElement()
{
    Symbol=0;
    Index=0;
}
//---------------------------------------------------------------------------
TbElement::TbElement(const char &Symbol)
{
    this->Symbol=Symbol;
    Index=0;
}
//---------------------------------------------------------------------------
TbElement::TbElement(const char &Symbol,const char &Index)
{
    this->Symbol=Symbol;
    this->Index=Index;
}
