TLessons::TLessons(const wchar_t *fileName)
{
    ifstream ifs(fileName);
    ifs>>count;
    if(count>0)
    {
        char buf[1024];
        this->fileName=new String [count];
        ifs.getline(buf,1024);
        for(int i=0;i<count;i++)
        {
            ifs.getline(buf,1024);
            this->fileName[i]=buf;
        }
    }
    else
    {
        this->fileName=NULL;
    }
    ifs.close();
    curr=0;
}
//---------------------------------------------------------------------------
TLessons::~TLessons()
{
    if(count>0)
    {
        delete fileName;
        count=0;
        fileName=NULL;
    }
}
//---------------------------------------------------------------------------
bool TLessons::next()
{
    if(curr<count)
    {
        curr++;
    }
    return curr<count;
}
