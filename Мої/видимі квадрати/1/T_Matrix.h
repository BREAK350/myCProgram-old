class T_Matrix
{
    private:
        int N,M,**E;
    public:
        T_Matrix();
        T_Matrix(const T_Matrix &Matrix);
        T_Matrix(const int &n,const int &m);
        T_Matrix(const int &n,const int &m,const int &e);
        ~T_Matrix();
        T_Matrix& operator = (const T_Matrix &Matrix);
        int GetE(const int &i,const int &j);
        void SetE(const int &i,const int &j,const int &e);
        int GetN() { return N; }
        int GetM() { return M; }
        void Nullify();
};
//---------------------------------------------------------------------------
class T_Matrix_Odd:public T_Matrix
{
    public:
        T_Matrix_Odd():T_Matrix(1,1) {}
        T_Matrix_Odd(const int &n,const int &m):T_Matrix(2*n+1,2*m+1) {}
        T_Matrix_Odd(const int &n,const int &m,const int &e):T_Matrix(2*n+1,2*m+1,e) {}
        T_Matrix_Odd(const T_Matrix_Odd &Matrix_Odd):T_Matrix(Matrix_Odd) {}

};
