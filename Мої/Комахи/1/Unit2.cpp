#include "Unit2.h"
#pragma package(smart_init)
//---------------------------------------------------------------------------
T_Insect::T_Insect(const char *n,const T_Life &l,const T_Stomach &s,
                    const T_Character &c,const T_Energy &e)
                    :Life(l),Character(c),Stomach(s),Energy(e),Name(n)
{
}
//---------------------------------------------------------------------------
T_Insect::T_Insect()
{
}
//---------------------------------------------------------------------------
void T_Insect::Attack_Simple(T_Insect &Insect)
{
    randomize();
    T_Attack a=GetAttack();
    T_Protection z=Insect.GetProtection();
    a.Interaction(z);
    Insect.Life.Interaction(a);
    Insect.Character.Interaction(a);
    Insect.Stomach.Interaction(a);
}
//---------------------------------------------------------------------------
T_Insect::T_Insect(const T_Insect &i):Name(i.Name),Life(i.Life),Stomach(i.Stomach),
    Character(i.Character),Energy(i.Energy),SW(i.SW)
{
}
//---------------------------------------------------------------------------
T_Insect& T_Insect::operator = (const T_Insect &i)
{
    if(this!=&i)
    {
        Name=i.Name;
        Life=i.Life;
        Stomach=i.Stomach;
        Character=i.Character;
        Energy=i.Energy;
        SW=i.SW;
    }
    return *this;
}
