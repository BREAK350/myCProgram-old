void Init_Gun(const int &n,T_Gun **&G)
{
    TRect R[]=
        {
            TRect(0,0,SizePicture,SizePicture),
            TRect(SizePicture,0,2*SizePicture,SizePicture),
        };
    T_Animation AnimG(Anim_POINTER,NULL,2,R);
    G[0]=new T_Gun(T_Point(0,0),AnimG,50,0,3);
    G[0]->Anim.LoadPicture("Picture//G1.bmp");
}
