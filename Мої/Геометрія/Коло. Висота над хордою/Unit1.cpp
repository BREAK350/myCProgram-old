int main(int argc, char* argv[])
{
    int n=5;
    double r=1,R,a;
    while(n<=100)
    {
        a=2*M_PI/n;
        R=2*r/(1-cos(a));
        printf("n=%3d  R>=%4.5f  min distance>=%4.5f\n",n,R,2*R*sin(a/2));
        n++;
    }
    getch();
    return 0;
}
