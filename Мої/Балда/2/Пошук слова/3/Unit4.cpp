int bT_Stack::Write(const TPoint &p)
{
    int q=0;
    if(Top<MAX_WORD)
    {
        q=1;
        Stack[Top]=p;
        Top++;
    }
    return q;
}
//---------------------------------------------------------------------------
int bT_Stack::Read(TPoint &p)
{
    int q=0;
    if(Top>0)
    {
        q=1;
        Top--;
        p=Stack[Top];
    }
    return q;
}
//---------------------------------------------------------------------------
void bT_Stack::Draw(TCanvas *Canvas,const TPoint &c,const int &s)const
{
    int i;
    for(i=Top-1;i>=0;i--)
    {
        Canvas->Rectangle(Stack[i].x*s+c.x-s/3.0,Stack[i].y*s+c.y-s/3.0,
                          Stack[i].x*s+s+c.x-s/3.0,Stack[i].y*s+s+c.y-s/3.0);
    }
}
