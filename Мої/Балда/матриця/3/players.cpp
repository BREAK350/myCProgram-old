//---------------------------------------------------------------------------
T_Players::T_Players(const int &n)
{
    N=(n>1)?(n):(1);
    Player=new T_Player[N];
    Curr=0;
}
//---------------------------------------------------------------------------
T_Players::T_Players()
{
    N=2;
    Player=new T_Player[N];
    Curr=0;
}
//---------------------------------------------------------------------------
T_Players::~T_Players()
{
    delete Player;
    Player=NULL;
    N=Curr=0;
}
//---------------------------------------------------------------------------
T_Player* T_Players::GetCurrPlayer()
{
    T_Player *r;
    if(Curr>=N)
    {
        Curr=0;
    }
    r=&Player[Curr];
    Curr+=1;
    return r;
}
//---------------------------------------------------------------------------
void T_Players::SetN(const int &n)
{
    delete Player;
    N=(n>1)?(n):(1);
    Player=new T_Player[N];
    Curr=0;
}
//---------------------------------------------------------------------------
T_Player* T_Players::GetPlayer(const int &i)
{
    T_Player *r=NULL;
    if(i>=0 && i<N)
    {
        r=&Player[i];
    }
    return r;
}
