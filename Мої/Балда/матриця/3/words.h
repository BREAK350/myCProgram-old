class T_Words
{
    private:
        struct T_Item
        {
            string Word;
            T_Item *Next;
            T_Item(const char *w):Word(w),Next(0) { }
        }*First,*Curr,*Last;
        void Delete(T_Item *&item);
    public:
        T_Words():First(0),Curr(0) { }
        ~T_Words() { Delete(First); Curr=NULL; }
        void GetStarted() { Curr=First; }
        int GetCurrWord(string &word);
        int Write(const char *w);
        void Clear() { Delete(First); Curr=NULL; }
        int Search(const char *w);
};
