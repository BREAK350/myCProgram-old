class T_Words
{
    private:
        struct T_Item
        {
            string Word;
            T_Item *Next;
            T_Item(const char *w):Word(w),Next(0) { }
        }*First,*Curr,*Last;
        int N;
        void Delete(T_Item *&item);
    public:
        T_Words():First(0),Curr(0) { N=0; }
        ~T_Words() { Delete(First); Curr=NULL; N=0; }
        void GetStarted() { Curr=First; }
        int GetCurrWord(string &word);
        int Write(const char *w);
        void Clear() { Delete(First); Curr=NULL; N=0; }
        int Search(const char *w);
        void SaveToFile(ofstream &ofs);
        void LoadFromFile(ifstream &ifs);
};
