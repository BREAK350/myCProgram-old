/*
    повертає:
        -1 - якщо не прочитало ";"
        0 - якщо виникла помилка
        >0 - довжина прочитаних символів
    Стрічка line розпочинається новим записом або крапкою
*/
int Words::init(char *line) // line!=NULL && line[0]!=0
{
    int res=-1;
    if(line[0]=='.')
    {
        res=0;
    }
    else
    {
        int i=0,apersant=0;
        while(line[i] && line[i]!='&')
        {
            i++;
        }
        if(line[i]=='&')
        {
            apersant=i;
            while(line[i] && line[i]!=';')
            {
                i++;
            }
            if(line[i]==';')
            {
                res=i+1;
                line[apersant]=0;
                eng=line;
                line[apersant]='&';
                line[i]=0;
                ukr=&line[apersant+1];
                line[i]=';';
            }
        }
    }
    return res;
}
