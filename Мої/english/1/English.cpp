#pragma package(smart_init)
//---------------------------------------------------------------------------
word::word()
{
    eng=NULL;
    n=0;
    ukr=NULL;
}
//---------------------------------------------------------------------------
word::word(char *_eng,int _n,char **_ukr)
{
    eng=_eng;
    n=_n;
    ukr=_ukr;
}
//---------------------------------------------------------------------------
ostream &operator<<(ostream &out,word w)
{
    int i;
    out<<w.eng<<" = ";
    for(i=0;i<w.n;i++)
    {
        out<<w.ukr[i]<<"; ";
    }
    return out;
}
//---------------------------------------------------------------------------
word::~word()
{
    int i;
    delete[] eng;
    for(i=0;i<n;i++)
    {
        delete[] ukr[i];
    }
}
