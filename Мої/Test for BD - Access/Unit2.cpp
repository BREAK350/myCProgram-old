String createConditionFor(const String &value,const Words &words)
{
	String condition;
	int size = words.size();
	if(size > 0)
	{
		condition = "(";
		int i = 0;
		for(; i < size - 1; i++)
		{
			condition = condition + value + "='" + words[i] + "' or ";
		}
		condition = condition + value + "='" + words[i] + "')";
	}
	return condition;
}
