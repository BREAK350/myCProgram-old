bool T_Object::CrossTRect(TRect A,TRect B)
{
    TRect C;
    if(A.Left>B.Left)
    {
        C=A;
        A=B;
        B=C;
    }
    if(B.Left>A.Right || B.Bottom<A.Top || B.Top>A.Bottom)
    {
        return false;
    }
    return true;
}
//---------------------------------------------------------------------------
bool T_Object::Cross(T_Object *obj)
{
    return CrossTRect(ReturnTRect(),obj->ReturnTRect());
}
