#pragma hdrstop
#include "T_Poligon.h"
#pragma package(smart_init)
//---------------------------------------------------------------------------
T_Poligon::T_Poligon()
{
    n=0;
    p=NULL;
    clPen=clBlack;
    clBrush=clWhite;
}
//---------------------------------------------------------------------------
T_Poligon::T_Poligon(int _n,TColor _clPen,TColor _clBrush,T_Point *_p)
{
    int i;
    if(_n<=0)
    {
        n=0;
        p=NULL;
    }
    n=_n;
    p=new T_Point[n];
    for(i=0;i<n;i++)
    {
        p[i]=_p[i];
    }
    clPen=_clPen;
    clBrush=_clBrush;
}
//---------------------------------------------------------------------------
T_Poligon::~T_Poligon()
{
    Delete();
}
//---------------------------------------------------------------------------
T_Point T_Poligon::operator [] (int i)
{
    if(i<0 || i>=n)
    {
        return p[0];
    }
    return p[i];
}
//---------------------------------------------------------------------------
void T_Poligon::Delete()
{
    delete[] p;
    p=NULL;
    n=0;
    clPen=clBlack;
    clBrush=clWhite;
}
//---------------------------------------------------------------------------
void T_Poligon::Init(int _n,TColor _clPen,TColor _clBrush,T_Point *_p)
{
    int i;
    if(_n<=0)
    {
        return;
    }
    Delete();
    n=_n;
    p=new T_Point[n];
    for(i=0;i<n;i++)
    {
        p[i]=_p[i];
    }
    clPen=_clPen;
    clBrush=_clBrush;
}
//---------------------------------------------------------------------------
T_Poligon::T_Poligon(const T_Poligon &Pol)
{
    int i;
    n=Pol.n;
    for(i=0;i<n;i++)
    {
        p[i]=Pol.p[i];
    }
    clPen=Pol.clPen;
    clBrush=Pol.clBrush;
}
//---------------------------------------------------------------------------
T_Poligon& T_Poligon::operator = (const T_Poligon &Pol)
{
    int i;
    if(this==&Pol)
    {
        return *this;
    }
    Delete();
    n=Pol.n;
    for(i=0;i<n;i++)
    {
        p[i]=Pol.p[i];
    }
    clPen=Pol.clPen;
    clBrush=Pol.clBrush;
    return *this;
}
